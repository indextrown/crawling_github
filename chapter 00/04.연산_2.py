# 비교 연산
print("비교연산 문제")
print(2 > 3)                             # f
print(15 < 30)                           # t
print(1.5 >= 0)                          # t
print(3 <= 3)                            # t
print("월천만원"=="월천만원")               # t
print("연봉1억"=="연봉10억")               # f

# 2 논리 연산
print("논리 연산 문제")
print(4 < 6 and 10 >= 10)                # t
print("포기" != "포기" or "안해" == "안해") # t
print(not 5 == 5)                        # f

